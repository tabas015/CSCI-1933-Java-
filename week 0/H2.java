// H2.java
// A second version of Hello World 

// Uses a String *variable* in place of the String
// *literal* "Hello World!".
// In Java, like Python, a String is an object.
// Java Strings are *immutable*

public class H2 {

    public static void main(String[] args) {
        String aPhrase = "Hello World!";
        System.out.println(aPhrase);
    }  // main

}  // H2 class
